package ru.kpfu.itis.form;

public class CountForm {

    private long accounting;
    private int count;

    public long getAccounting() {
        return accounting;
    }

    public void setAccounting(long accounting) {
        this.accounting = accounting;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
